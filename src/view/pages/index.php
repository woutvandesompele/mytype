      Bodoni was born in Piedmont, Saluzzo. His father and grandfather were both printers in Piedmont. At the age of 17 he travelled to Rome where he mastered the skill of printing. After five years of learning he planned to move to England, which typography then florished under Baskerville's influence. But unfortunatly that year he succumbed to Tertian fever (Malaria). This lead him back to Saluzzo.<br><br>

